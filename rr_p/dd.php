function fsubmit2(f)
{
	if(!$("#q_date").val()){
		alert(cate + " 서비스일을 선택해주세요");
		return false;
	}
	var cate = $("#q_cate").val();
	if(!cate){
		alert("서비스를 선택해주세요");
		return false;
	}
	if(!$("#q_sv").val()){
		alert(cate + " 서비스를 선택해주세요");
		return false;
	}
	
	var hpx = $("#q_hp2").val() + $("#q_hp3").val();
	var hp1x = hpx.substring(0,1);
	var hp2x = hpx.substring(1,2);
	var hp3x = hpx.substring(2,3);
	var hp4x = hpx.substring(3,4);
	var hp5x = hpx.substring(4,5);
	var hp6x = hpx.substring(5,6);
	var hp7x = hpx.substring(6,7);
	var hp8x = hpx.substring(7,8);
	
	if((hp1x == hp2x) && (hp1x == hp3x) && (hp1x == hp4x) && (hp1x == hp5x) && (hp1x == hp6x) && (hp1x == hp7x) && (hp1x == hp8x)){
		alert("연락처를 다시 확인해주세요");
		return false;
	}

	if(cate == "이사"){ 
		if(!$("#addr1").val()){
			alert("출발지 상세주소를 선택해주세요");
			return false;
		}
	}
	if(!$("#addr2").val()){
		alert("도착지 상세주소를 선택해주세요");
		return false;
	}
	$('.btnSubmit').attr('disabled', true);
	f.action = './update.php';
    return true;
}
